package aggregation_examples;

public class Ram {

	public int Id;
	public int Size;
	public int Price;
	public String Brand;
	public String Model;

	public void printInfo() {
		System.out.println("id : " + Id);
		System.out.println("brand : " + Brand);
		System.out.println("model : " + Model);
		System.out.println("price : " + Price);
		System.out.println("weight : " + Size);
	}

}
